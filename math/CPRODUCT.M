/* product: UU+i*VV = (AA+i*BB)*(CC+i*DD): */
# define CPRODUCT( AA, BB, CC, DD, UU, VV ) \
{ \
   (UU) = (AA)*(CC) - (BB)*(DD); \
   (VV) = (BB)*(CC) + (AA)*(DD); \
}
/*----------------------------------------------------------------------------*/
/* quotient: UU+i*VV = (AA+i*BB)/(CC+i*DD): */
# define CQUOTIENT( AA, BB, CC, DD, UU, VV ) \
{ \
   (VV) = (CC)*(CC) + (DD)*(DD); \
   (UU) = ((AA)*(CC) + (BB)*(DD))/(VV); \
   (VV) = ((BB)*(CC) - (AA)*(DD))/(VV); \
}
